//static function 说明该函数仅为当前编译单元编译,保证不会被其他cpp文件调用
//.h文件被引用时真的只是复制粘贴到了该编译单元里,本身是不会被编译的,多个文件引用时会使某些不能重复的代码重复编译(比如同名函数的函数体)
//一般.h文件只写声明,在同名.cpp文件里实现它;
//inline(内联) function 说明该函数当被调用时只是使用其函数体语句添加到代码中,而不是再正儿八经地声明与定义并在执行时跳转到该函数指令




